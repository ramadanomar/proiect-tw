export const deleteTeacher = async (
  currentState: CurrentState,
  data: FormData
) => {
  const id = data.get("id") as string;

  const teacherObject = await prisma.teacher.findUniqueOrThrow({
    where: {
      id,
    },
  });

  const teacherEmail = teacherObject.email as string;

  console.log("teacher email!", teacherEmail);
  try {
    const clerkUserObject = await clerkClient().users.getUserList({
      emailAddress: [teacherEmail],
    });

    console.log("Clert User Object", clerkUserObject);
    clerkUserObject.data.forEach(async (user) => {
      console.log("UserID", user);
      await clerkClient().users.deleteUser(user.id);

      console.log(`deleted user in clerk ${user.id}`, data);
    });

    await prisma.teacher.delete({
      where: {
        id: id,
      },
    });

    // revalidatePath("/list/teachers");
    return { success: true, error: false };
  } catch (err) {
    console.log(err);
    return { success: false, error: true };
  }
};

export const createStudent = async (
  currentState: CurrentState,
  data: StudentSchema
) => {
  console.log(data);
  try {
    const classItem = await prisma.class.findUnique({
      where: { id: data.classId },
      include: { _count: { select: { students: true } } },
    });

    if (classItem && classItem.capacity === classItem._count.students) {
      return { success: false, error: true };
    }

    const user = await clerkClient().users.createUser({
      emailAddress: [data.email ? data.email : `${data.username}@gmail.com`],
      username: data.username,
      password: data.password,
      firstName: data.name,
      lastName: data.surname,
      publicMetadata: { role: "student" },
    });

    await prisma.student.create({
      data: {
        id: user.id,
        username: data.username,
        name: data.name,
        surname: data.surname,
        email: data.email || null,
        phone: data.phone || null,
        address: data.address,
        img: data.img || null,
        bloodType: data.bloodType,
        sex: data.sex,
        birthday: data.birthday,
        classId: data.classId,
      },
    });

    // revalidatePath("/list/students");
    return { success: true, error: false };
  } catch (err) {
    console.log(err);
    return { success: false, error: true };
  }
};

